#Arthimetic Operators
"""
+    Addition
-    Subtraction
*    Multiplication
/    Division
%    Modulus
**   Exponentiation
//   Floor division
"""

#Logical Operators
""" 
and-  Logical AND:True if both the operands are true 
or-   Logical OR:True if either the operands is true 
not-  Logical NOT:True if operands is false

"""

#Comparison Operators
"""

== : equal to e.g 5==5 True
!= : equal to e.g 8 !=5 True
>  : greater than e.g 3 > 10 False
<  : less than e.g 5 < 8  True
>= : greater than or equal to e.g 5>= 10 False
<= : less than or equal to e.g 5 <= True
"""



#Assignment Operators
"""
OPERATOR   EXAMPLE   EXPLANATION
=          X=5       X=5
+=         X+=3      X=X+3
-=         X-=3      X=X-3
*=         X*=3      X=X*3
 /=        X/=3      X=X/3
 %=        X%=3      X=X%3
 //=       X//=3     X=X//3
"""

# Identity operators
"""
OPERATOR     EXAMPLE                                                    EXPLANATION
 is          Returns true if both variables are the same object         x is y
 is not      Returns true if both variables are not the same object     x is not y
"""


# Membership Operators
"""
in       Returns True if a sequence with the specified value is present in an object        x in y
not in   Returns True if a sequence with the specified value is not  present in an object   x not in y
"""